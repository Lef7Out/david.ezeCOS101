fn main() {
    // addition
    let addition: u32 = 5550 + 7310;
    println!("The sum of 5550 and 7310 = {}", addition);

    // addition
    let difference: f32 = 95.5 - 4.3;
    println!("The difference of 95.5 and 4.3 = {}", difference);

    //Multiplication
    let product: i32 = 4 * 30;
    println!("The product of 5 and 30 = {}", product);

    //Division
    let quotient: f64 = 56.7 / 32.2;
    println!("The division of 56.7 and 32.2 = {:.3}", quotient);

    //remainder
    let remainder  = 43 % 5; 
    println!("The remainder of 43 and 5 = {}", remainder);
}
